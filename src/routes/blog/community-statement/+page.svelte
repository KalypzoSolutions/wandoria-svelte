<p>
    Unsere Entscheidung, uns vom ursprünglichen Projekt zu trennen handelt von kontinuierlicher
    Unzufriedenheit mit dem Projektmanagement:
</p>
<ul>
    <li>
        Monatelanger Stillstand von Wandoria, stattdessen lag der Fokus auf Akani, danach Tandara.
    </li>
    <li>
        Verzögerte Zahlungen und <em>in unseren Augen</em> unverhältnismäßige oder verschwenderische Ausgaben
        sorgten oft für Frust.
    </li>
    <li>
        Das Beta-Drama: Mehrfach angekündigte Beta-Starts wurden kurzfristig verschoben oder abgesagt,
        ohne die Spieler umfassend zu informieren. Wenn wir uns Recht entsinnen gab uns nur eine Beta,
        danach ging die Entwicklung von Akani los.
    </li>
    <li>
        Mangelndes technisches Know-how und unspezifische Anforderungen machte uns die Arbeit unnötig
        schwer. Man war plötzlich Server-Verwaltung und Plugin-Tester, statt Plugin-Entwickler.
        Ständig hatte man mit vermeidbaren Fehlern zu kämpfen oder musste sich um viele Kleinigkeiten
        kümmern.
    </li>
</ul>
<p>
    Dennoch müssen wir Feli andere Fähigkeiten zugestehen. Er ist kreativ, ein Marketing-Ass und
    schafft es hervorragend die User an sich zu binden. Er war stets offen für Gespräche, jedoch
    hatten wir nach all dem Leid nicht die Bereitschaft etwas zu ändern, weil wir den Eindruck
    hatten, dass wir mit Feli nie ans Ziel gelangen würden. <br/>
    <strong
        >Die Wandoria-Entwicklung, wirkte auf uns, all die Monate nach der Beta von Februar bis zu unseren
        Verlassen des Teams im September eingefroren.</strong
    >  <s>Was im Hintergrund all die Zeit womöglich geschah wissen wir nicht. Wir vermuten, dass wenig passiert ist, da der Fokus auf Akani lag.</s>*

</p>
<p>*Feli betonte im Nachhinein, dass parallel zur Akani Entwicklung die custom Mobs im Hintergrund für Wandoria modelliert und programmiert wurden. 
    Der Bau der neuen Lobby lief fortlaufend weiter. Nur hat er existierende Developer nicht genutzt und sie verweilten ohne Aufgabe und Statusupdates auf dem Discord.
</p>
